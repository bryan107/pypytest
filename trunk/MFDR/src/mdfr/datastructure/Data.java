package mdfr.datastructure;

public class Data {
	private double time;
	private double value;
	
	public Data(double time, double value){
		this.time = time;
		this.value = value;
	}
	
	public double time(){
		return time;
	}
	
	public double value(){
		return value;
	}
	
	
	
}
